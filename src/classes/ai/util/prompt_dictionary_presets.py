def _dedupe(values: list[str]) -> list[str]:
    unique = []
    seen = set()
    for value in values:
        text = str(value or "").strip()
        lowered = text.casefold()
        if not text or lowered in seen:
            continue
        seen.add(lowered)
        unique.append(text)
    return unique


def _add_entry(target: dict[str, list[str]], canonical: str, aliases: list[str]) -> None:
    normalized_key = str(canonical or "").strip().lower().replace(" ", "_").replace("-", "_")
    if not normalized_key:
        return
    existing = target.get(normalized_key, [])
    target[normalized_key] = _dedupe(list(existing) + list(aliases))


def _build_variant_aliases(names: list[str], variant_en: str, variant_ja: str) -> list[str]:
    aliases = []
    for name in names:
        label = str(name or "").strip()
        if not label:
            continue
        aliases.append(f"{label} {variant_en}")
        aliases.append(f"{label}{variant_ja}")
        aliases.append(f"{variant_ja}{label}")
    return _dedupe(aliases)


@lru_cache(maxsize=1)
def build_prompt_dictionary_preset_config() -> dict:
    general_aliases: dict[str, list[str]] = {}
    allowlist = _dedupe(_ALLOWLIST_TERMS)

    for symbol, english, japanese in _ELEMENTS:
        aliases = _dedupe([symbol, english, japanese, english.title()])
        _add_entry(general_aliases, symbol.lower(), aliases)
        if symbol in _ROOM_TEMPERATURE_NON_SOLID_ELEMENTS:
            continue
        for variant_id, variant_en, variant_ja in _VARIANTS:
            _add_entry(
                general_aliases,
                f"{symbol.lower()}_{variant_id}",
                _build_variant_aliases(aliases, variant_en, variant_ja),
            )

    for canonical, aliases in _MATERIALS:
        cleaned_aliases = _dedupe(list(aliases))
        _add_entry(general_aliases, canonical, cleaned_aliases)
        for variant_id, variant_en, variant_ja in _VARIANTS:
            _add_entry(
                general_aliases,
                f"{canonical}_{variant_id}",
                _build_variant_aliases(cleaned_aliases, variant_en, variant_ja),
            )

    for canonical, aliases in _CHARACTERIZATION_AND_PROCESS_TERMS:
        _add_entry(general_aliases, canonical, _dedupe(list(aliases)))

    for canonical, aliases in _DEVICE_TERMS:
        _add_entry(general_aliases, canonical, _dedupe(list(aliases)))

    return {
        "general_aliases": general_aliases,
        "allowlist": allowlist,
        "allowlists": {"global": allowlist, "sources": {}},
        "metadata": {
            "preset_seed_name": "materials_device_aliases",
            "preset_general_alias_count": len(general_aliases),
            "preset_allowlist_count": len(allowlist),
        },
    }
